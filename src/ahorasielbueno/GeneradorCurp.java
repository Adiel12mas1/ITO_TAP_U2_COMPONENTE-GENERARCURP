    private void cboEstadoMouseEntered(java.awt.event.MouseEvent evt) {                                       
//System.out.println("Entra al metodo de leer archivo");
        // TODO add your handling code here:
        File archivo = new File("Estados.csv");
        if (archivo.exists()) {
            try {
                BufferedReader reader = new BufferedReader(new FileReader(archivo));
                String linea;

                while ((linea = reader.readLine()) != null) {
                    String[] atributos = linea.split(";");
                    String estado = atributos[0].trim();
                    String abreviacion= atributos[1].trim();
                    cboEstado.addItem(estado);
                    cboEstado.putClientProperty(estado, abreviacion);
                }
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        } else {
            try {
                archivo.createNewFile();

            } catch (IOException ex) {
                System.out.println(ex + "Error el archivo no Existe");
            }
        }
    }                                      

