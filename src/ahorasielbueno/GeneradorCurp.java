    public char obtenerPrimeraConsonante(String texto) {
    char[] consonantes = {'B', 'C', 'D', 'F', 'G', 'H', 'J', 'K', 'L', 'M', 'N', 'Ñ', 'P', 'Q', 'R', 'S', 'T', 'V', 'W', 'X', 'Y', 'Z'};
    char[] vocales = {'A', 'E', 'I', 'O', 'U'};

    texto = texto.toUpperCase();

    boolean primeraVocalEncontrada = false;
    for (int i = 0; i < texto.length(); i++) {
        char letra = texto.charAt(i);
        
        
        if (!primeraVocalEncontrada) {
            for (char vocal : vocales) {
                if (letra == vocal) {
                    primeraVocalEncontrada = true;
                    break;
                }
            }
        } else {
           
            for (char consonante : consonantes) {
                if (letra == consonante) {
                    return letra; // Devuelve la primera consonante después de la vocal
                }
            }
        }
    }
    
    return '\0'; 
}

 public char encontrarPrimeraVocal(String texto) {
    char[] vocales = {'A', 'E', 'I', 'O', 'U'};

    texto = texto.toUpperCase();

    for (int i = 0; i < texto.length(); i++) {
        char letra = texto.charAt(i);
        
        for (char vocal : vocales) {
            if (letra == vocal) {
                return letra;
            }
        }
    }
    
    return '\0'; 
}
  
     
